$(document).ready(function() {
	
	dymonav.ToggleNavigationSlider();
	dymonav.opts.next_btn.click(function() {
	   dymonav.opts.nav.animate({'left' : '-=200px'}, 1000);
		setTimeout(function() {
			if(dymonav.opts.nav.css('left') < '-36px'){
				dymonav.opts.arrows.parent().find('#prev').fadeIn();
			}
		}, 1700);
		
	   setTimeout(function(){
	   	 dymonav.opts.arrows.parent().find('#next').fadeOut();
	   }, 1000);
	});
	dymonav.opts.prev_btn.click(function(){
		dymonav.opts.nav.animate({'left' : '0'}, 1000);
		setTimeout(function(){
			dymonav.opts.arrows.parent().find('#prev').fadeOut();
			dymonav.opts.arrows.parent().find('#next').fadeIn();
		}, 1000);
	})
});
